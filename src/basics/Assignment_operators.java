package basics;

public class Assignment_operators {

	public static void main(String[] args) {
		int a=55;
		System.out.println(a);
		int b=a;
		System.out.println(b);
		b+=a;		//b=b+a
		System.out.println(b);
		b-=a;		//b=b-a
		System.out.println(b);
		b*=a;		//b=b*a
		System.out.println(b);
		b/=a;		//b=b/a
		System.out.println(b);
	}

}
